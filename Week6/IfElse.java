public class IfElse {

    public static void main(String[] args) {
        // if else

            if(true){
                System.out.println("This is true");
            }else{
                System.out.println("This is false");
            }

            if(false){
                System.out.println("This is true");
            }
            
            if(true){
                System.out.println("This is true");
            }else if(false){
                System.out.println("This is false");
            }else{
                System.out.println("This is else");
            }
        // Block scoped variables
        
    }
    
}
