package Week5;

public class Operators {

    public static void main(String[] args) {
        // Arithmetic Operators
        // +, -, *, /, %

        // There is a shorthand way to use these operators, which is called compound assignment operators
        // +=, -=, *=, /=, %=

        // += is used to add a value to a variable and assign the result back to the variable
        int a = 5;
        a += 3; // This is the same as a = a + 3 = 5 + 3 = 8, so now a is 8
        System.out.println(a);  

        // -= is used to subtract a value from a variable and assign the result back to the variable
        int b = 10; 
        b -= 4; // This is the same as b = b - 4 = 10 - 4 = 6, so now b is 6
        System.out.println(b);

        // *= is used to multiply a variable by a value and assign the result back to the variable
        int c = 7;
        c *= 2; // This is the same as c = c * 2 = 7 * 2 = 14, so now c is 14   
        System.out.println(c);

        // /= is used to divide a variable by a value and assign the result back to the variable
        int d = 20;
        d /= 5; // This is the same as d = d / 5 = 20 / 5 = 4, so now d is 4
        System.out.println(d);

        
    }
    
}
